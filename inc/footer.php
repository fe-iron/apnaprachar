    	if ($(this).scrollTop() > 100) {
    		$('.scrollToTop').fadeIn();
    	} else {
    		$('.scrollToTop').fadeOut();
    	}
    });
    
    //Click event to scroll to top
    $('.scrollToTop').click(function(){
    	$('html, body').animate({scrollTop : 0},500);
    	return false;
    });
    
});


